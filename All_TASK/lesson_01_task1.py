def main():
    # Ask for user's name
    name = input("What is your name? ")

    # Ask for user's age
    age = input("How old are you? ")

    # Ask for user's city
    city = input("Where do you live? ")

    # Display responses
    print(f"Hello, {name}.")
    print(f"Your age is {age}.")
    print(f"You live in {city}.")

if __name__ == "__main__":
    main()